public class EvenOdd {
    public static void main(String[] args)
    {
        System.out.println("To check the number is even or odd");
        int a = 5;
        if(a%2 == 0)
        {
            System.out.println("The number "+ a +" is even");
        }
        else
        {
            System.out.println("The number "+ a +" is odd");
        }
    }
}
